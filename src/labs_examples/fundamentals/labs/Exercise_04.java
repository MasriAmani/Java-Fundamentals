package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        byte a  = 1;
        System.out.println("byte a is: " + a);

        short b  = 2;
        System.out.println("short b is: " + b);

        int c = 3;
        System.out.println("int c is: " + c);

        long d  = 4;
        System.out.println("long d is: " + d);

        float e  = 5;
        System.out.println("float e is: " + e);

        double f = 6;
        System.out.println("double f is: " + f);

        char g  = '@';
        System.out.println("char g is: " + g);

        boolean h = true;
        System.out.println("boolean h is: " + h);


    }

}
